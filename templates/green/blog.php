                đặc biệt là nhà thờ không có vòng rào hoặc bờ tường bao quanh như
                các nhà thờ quanh vùng Sài Gòn – Gia Định lúc ấy và bây giờ.
            </p>
            <p>
                <img src="/green/templates/green/images/Blog/notre_dame_cathedral_2.jpg">
