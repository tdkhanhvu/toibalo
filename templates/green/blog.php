            <h2>Dinh Độc Lập</h2>
            <p>
                <b>Kiến trúc Dinh Thống Nhất – Dinh Độc Lập</b>
            </p>

            <p>
                Khi nói đến Sài Gòn với những di tích kiến trúc có tính đặc trưng, độc đáo
                không ai không liên tưởng đến Dinh Thống Nhất với vai trò lịch sử đối với
                dân tộc Việt Nam, vị trí được xác lập trong giao lưu kinh tế, chính trị,
                xã hội đối với toàn vùng Ðông Nam á cũng như trên thế giới.
            </p>
            <p>
                <img src="/green/templates/green/images/Blog/independence_palace_1.jpg">
            </p>
            <p>
                Mặt tiền của Dinh Thống Nhất nằm trên ngã ba đường Nam Kỳ Khởi Nghĩa và
                đường Lê Duẩn. Khuôn viên rộng 15ha. Trên mặt bằng này, ngày 23.2. 1868,
                đô đốc thực dân Pháp De lagrandière làm lễ đặt viên đá đầu tiên, đặt tên
                là Dinh Norodom. Dinh Norodom được xây dựng và hoàn thành sau đó 3 năm,
                đây là cơ quan biểu thị cho bộ máy cai trị Pháp trên toàn cõi Ðông Dương.
            </p>

            <p>
                Thời Pháp thuộc đây là trụ sở của các vị thủ hiến người Pháp được dân Nam
                gọi là dinh Thống Soái, và người Pháp gọi là Palais Norodom. Năm 1865 tờ
                Courier de Saigon viết: “Những nghiên cứu đầu tiên liên quan đến việc xây
                cất một dinh thự dành làm trụ sở vĩnh viễn cho quan Thống đốc bắt đầu thực
                hiện để thay thế cho những nhà bằng ống tạm thời”. Và dinh Thống Soái là
                một trong những dinh thự đồ sộ được xây dựng với mục đích đó, theo bản vẽ
                của kiến trúc sư Hermite (Hermite cũng là người phác thảo đố án tòa thị
                sảnh Hương Cảng). Theo sử liệu, viên đá đầu tiên của dinh Thống Soái do
                đô đốc De Lagrandière tự tay đặt vào ngày 23-2-1863. Viên đá lịch sử này
                là khối đá lấy ở Biên Hòa, hình vuông rộng mỗi cạnh 50cm, có lỗ, bên trong
                chứa những đồng tiền hiện hành thuở ấy bằng vàng, bạc, đồng, có chạm hình
                Napoléon đệ tam.
            </p>
            <p>
                Vật tư xây cất phần lớn được chuyển từ Pháp sang. Do chiến tranh Pháp-Ðức
                (xảy ra năm 1870) nên công trình này kéo dài mãi đến năm 1873 mới xong,
                riêng việc trang trí dinh Độc Lập phải kéo dài hai năm sau (1875). Lúc
                bấy giờ đại lộ Thống Nhất (nay là đường Lê Duẩn) còn gọi là đại lộ
                Norodom vẫn còn bùn lầy, ẩm thấp. Dinh này được coi là một công thự
                đẹp nhất ở Á Ðông. Mặt tiền rộng 80m, bên trong có phòng khách có thể chứa
                đến 800 người. Chung quanh là khu vườn rộng lớn trồng đủ loại cây cỏ
                rất ngoạn mục. Ngay trước mặt dinh Thống Nhất, dưới chân cột cờ có đặt
                một khẩu thần công kiểu cổ càng làm tăng thêm vẻ đẹp oai nghiêm cho công thự.
            </p>
            <p>
                <img src="/green/templates/green/images/Blog/independence_palace_2.jpg">
            </p>
            <p>
                Nhiều biến cố chính trị đã xảy ra tại đây suốt gần một thế kỷ dưới sự
                thống trị của thực dân Pháp. Sau thất bại Ðiện Biên Phủ và Hiệp định
                Genève 1954, Dinh Norodom được trao lại cho Việt Nam. Năm 1954, Ngô
                Ðình Diệm được người Mỹ đặt ngồi vào Dinh Norodom. Ngày 26/10/1956
                Dinh Norodom đổi tên là “Dinh Ðộc Lập”, tức Phủ Tổng Thống. Sau ngày
                hai viên phi công của không quân Sài Gòn là Nguyễn Văn Cử và Phạm Phú
                Quốc ném bom xuống Dinh Ðộc Lập (ngày 27/02/1962) làm thiệt hại nặng
                nề. Vì lối kiến trúc quá cổ và vị hư hại nhiều nên Diệm quyết định phá
                hủy toàn bộ dinh Norodom để xây dựng lại một dinh hoàn toàn mới gọi
                là dinh Ðộc Lập.
            </p>

            <p>
                Thời gian xây cất kéo dài gần 3 năm: ngày 31-10-1966 việc xây dựng dinh hoàn
                tất. Trong thời gian xây dựng, có 6 tháng công việc bị đình trệ do nội bộ
                chính quyền Sài Gòn lục đục đánh nhau rồi đảo chính, nhất là sự kiện Ngô
                Ðình Diệm bị lật đổ và bị giết chết vào tháng 11-1963.
            </p>

            <p>
                <img src="/green/templates/green/images/Blog/independence_palace_3.jpg">
            </p>

            <p>
                Diện tích khu này khoảng 12ha, diện tích mặt bằng dinh khoảng 2.000m2.
                Tòa nhà có diện tích 4.500m2, gồm1 tầng nền, 3 tầng chính, 2 gác lửng,
                1 sân thượng đúc có thể làm bãi đáp cho máy bay trực thăng, trên 100
                phòng ốc, 4000 ngọn đèn, 400 đường dây điện thoại nội Dinh, 1 tầng hầm
                kiên cố, 1 đài phát thanh dự phòng, 1 phòng chỉ huy tác chiến….
                Tác giả công trình này là kiến trúc sư Ngô Viết Thụ, người từng đoạt
                giải Khôi Nguyên Kiến Trúc ở La Mã. Về kiến trúc, theo kiến trúc sư
                Ngô Viết Thụ thì từ sự xếp đặt bên trong cho đến phần tiền diện bên
                ngoài, tất cả đều tượng trưng cho một truyền thuyết cổ truyền phương
                đông, một nghi lễ thần thánh theo phong tục tập quán của dân tộc.
                Chúng ta có thể nhận thấy rằng, bình diện của toàn thể Dinh độc Lập
                được hình thành từ chữ Cát khi tâm của tòa nhà là phòng trình ủy
                nhiệm thư có thể so sánh như vị trí của điện Thái Hòa ở đại nội cố đô Huế.
            </p>

