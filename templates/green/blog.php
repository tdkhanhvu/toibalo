                chính là một hành lang và kế đó là nhiều nhà nguyện nhỏ với những bàn thờ
                nhỏ (hơn 20 bàn thờ) cùng các bệ thờ và tượng thánh nhỏ làm bằng đá trắng
                khá tinh xảo. Bàn thờ nơi Cung Thánh làm bằng đá cẩm thạch nguyên khối có
                hình sáu vị thiên thần khắc thẳng vào khối đá đỡ lấy mặt bàn thờ, bệ chia
                làm ba ô, mỗi ô là một tác phẩm điêu khắc diễn tả thánh tích.
            </p>
            <p>
                <img src="/green/templates/green/images/Blog/notre_dame_cathedral_3.jpg">
