            <p>
                Trong quá trình xây dựng, toàn bộ vật liệu xây dựng từ xi măng, sắt thép
                đến ốc vít đều mang từ Pháp sang. Mặt ngoài của công trình xây bằng
                loại gạch đặt làm tại Marseille để trần, không tô trát, (đến nay vẫn
                còn màu sắc hồng tươi), không bám bụi rêu. Một số ngói vỡ trong nhà
                thờ có in hàng chữ Guichard Carvin, Marseille St André France (có lẽ
                là nơi sản xuất loại ngói này), mảnh ngói khác lại có hàng chữ Wang-Tai
                Saigon. Có thể đây là mảnh ngói được sản xuất sau tại Sài Gòn dùng để
                thay thế những mảnh ngói vỡ trong thời gian Thế chiến thứ hai do
                những cuộc không kích của quân Đồng Minh. Toàn bộ thánh đường có 56 ô
                cửa kính màu do hãng Lorin của tỉnh Chartres (Pháp) sản xuất.
            </p>
            <p>
                Móng của thánh đường được thiết kế đặc biệt, chịu được tải trọng gấp
