                phụ tiếp đến là hai dãy nhà nguyện. Toàn bộ chiều dài thánh đường
                là 93m. Chiều ngang nơi rộng nhất là 35 m. Chiều cao của vòm mái
                thánh đường là 21 m. Sức chứa của thánh đường có thể đạt tới 1.200 người.
            </p>
            <p>
                Nội thất thánh đường nhà thờ Đức Bà có hai hàng cột chính hình chữ nhật,
