                sổ mắt bò bằng kính nhiều màu ghép lại với những hình ảnh rất đẹp.
                Tất cả các đường nét, gờ chỉ, hoa văn đều tuân thủ theo thức Roman và
                Gôtich, tôn nghiêm và trang nhã. Tuy nhiên, trong số 56 cửa kính này
                hiện chỉ còn bốn cửa là nguyên vẹn như xưa, còn các cửa kính màu khác
                đều đã được làm lại vào khoảng những năm 1949 để thay thế các cửa kính
                màu nguyên thủy của nhà thờ Đức Bà đã bị bể gần hết trong thế chiến
                thứ 2. Trên trán tường của cửa chính nhà thờ có hàng chữ Latinh:
            </p>
