    <p>
        Vùng đất này ban đầu được gọi là Prey Nokor, thành phố sau đó hình thành nhờ công
        cuộc khai phá miền Nam của nhà Nguyễn. Năm 1698, Nguyễn Hữu Cảnh cho lập phủ Gia Định,
        đánh dấu sự ra đời thành phố. Khi người Pháp vào Đông Dương, để phục vụ công cuộc khai
        thác thuộc địa, thành phố Sài Gòn được thành lập và nhanh chóng phát triển, trở thành
        một trong hai đô thị quan trọng nhất Việt Nam, được mệnh danh Hòn ngọc Viễn Đông hay
        Paris Phương Đông. Sài Gòn là thủ đô của Liên Bang Đông Dương giai đoạn 1887-1901.
        Năm 1954, Sài Gòn trở thành thủ đô của Việt Nam Cộng hòa và thành phố hoa lệ này
        trở thành một trong những đô thị quan trọng của vùng Đông Nam Á. Việt Nam Cộng hòa
        sụp đổ năm 1975, lãnh thổ Việt Nam hoàn toàn thống nhất. Ngày 2 tháng 7 năm 1976,
        Quốc hội nước Việt Nam thống nhất quyết định đổi tên Sài Gòn thành "Thành phố Hồ Chí Minh",
        theo tên vị Chủ tịch nước đầu tiên của Việt Nam Dân chủ Cộng hòa.
    </p>
